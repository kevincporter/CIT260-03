
package galtoliters;

public class GalToLiters {
  public static void main(String[] args) {
    double gallons; // holds the number of gallons
    double liters; 
    
    gallons = 10;
    liters = gallons * 3.7854;
    
    System.out.println(gallons + " gallons is " + liters + " liters.");
  }
}
